    getName(name) {
        return this.weapon_names[name.replace("weapon_", "")] || name;
    },
    getWeapons(lvl) {
        let self = this;
        let last_standart_weapons = "";
        let unlocks = [];
        let all_standartweapons = [];
        let level_unlocks = [];
        let level_standart = "";
        Object.keys(self.levels).forEach(function(level) {
            if (lvl >= level) {
                if (self.levels[level].standart) {
                    all_standartweapons.push(self.levels[level].standart);
                    last_standart_weapons = self.levels[level].standart;
                }
                if (self.levels[level].unlocks) {
                    unlocks = unlocks.concat(self.levels[level].unlocks)
                }
            }
            if (lvl == level) {
                level_unlocks = self.levels[level].unlocks;
                level_standart = self.levels[level].standart;
            }
        })
        return {
            standart: last_standart_weapons,
            standart_unlocked: all_standartweapons,
            unlocked_weapons: unlocks,
            level_standart: level_standart,
            level_unlocks: level_unlocks
        }
    },
